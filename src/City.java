public class City {
    private String name;
    private double square;


    public City(String name, double square){
        this.name = name;
        this.square = square;
    }


    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public double getSquare() {
        return square;
    }

    public void setSquare(double square) {
        this.square = square;
    }





}
